    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nombre = this.txtNombre.getText();
        String periodo = this.cbPeriodo.getSelectedItem().toString();
        
        String dias = "";
        if (this.rbLunes.isSelected())  dias +=  "Lu";     
        if (this.rbMartes.isSelected())  dias += "Ma";
        if (this.rbMier.isSelected()) dias += "Mi";
        if (this.rbJuev.isSelected()) dias += "Ju";
        if (this.rbVier.isSelected()) dias += "V";
        if (this.rbSab.isSelected()) dias += "Sab";
        
        String hora = this.txtHora.getText();
        
        boolean horaReg = Pattern.matches("^([01]?[0-9]|2[0-3]):[0-5][0-9]$", hora);
        
        if (horaReg){
            
            if ("".equals(dias) || periodo.isEmpty() || nombre.isEmpty()){
                JOptionPane.showMessageDialog(this, "Por favor rellene todos los campos");
            } else {
                try {
                    //Aquí va lo para agregar
                    Curso curso = new Curso(nombre, periodo, dias, hora);
                    guardar(Conexion.obtener(), curso);
                    txtNombre.setText("");
                    txtHora.setText("");
                    rbLunes.setSelected(false);
                    rbMartes.setSelected(false);
                    rbMier.setSelected(false);
                    rbJuev.setSelected(false);
                    rbVier.setSelected(false);
                    rbSab.setSelected(false);
                } catch (SQLException | ClassNotFoundException ex) {
                    Logger.getLogger(DlgAgregarCurso.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Por favor rellene la hora con sistema de 24 horas");
        }
        
               
    }//GEN-LAST:event_btnAgregarActionPerformed

