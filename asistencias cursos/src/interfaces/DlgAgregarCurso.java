    public void guardar(Connection conexion, Curso curso) throws SQLException{
      try{
         PreparedStatement consulta;
         
         if (conn.ifExists ("Select * from cursos where nombre = ? AND periodo = ? AND dias = ? AND hora = ?", 
                 curso.getNombre(), curso.getPeriodo(), curso.getDias(), curso.getHora())){
        //Existe registro
            JOptionPane.showMessageDialog(this, "El curso ya está registrado");
        }else{
        //No hay registros
            consulta = conexion.prepareStatement("INSERT INTO cursos (nombre, periodo, dias, hora) VALUES(?, ?, ?, ?);");
            consulta.setString(1, curso.getNombre());
            consulta.setString(2, curso.getPeriodo());
            consulta.setString(3, curso.getDias());
            consulta.setString(4, curso.getHora());
            consulta.executeUpdate();
            JOptionPane.showMessageDialog(this, "¡Se registró el curso con exito!");
            //conexion.close();
            
        }
           
      }catch(SQLException ex){
          JOptionPane.showMessageDialog(this, "Error con el registro");
         throw new SQLException(ex);
      }
      
      
   }

